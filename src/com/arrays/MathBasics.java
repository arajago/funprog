package com.arrays;

import static java.lang.Math.*;


public class MathBasics{
	//abs, ceil, floor, min, max, pow, sqrt
	public static void main(String[] args){
		System.out.println("max(1,2) "+max(1,2));
		System.out.println("ceil(5.3) "+ceil(5.3));
		System.out.println("floor(5.8) "+floor(5.8));
		System.out.println("abs(5.3) "+abs(5.3));
		System.out.println("abs(-5.3) "+abs(-5.3));
		System.out.println("round(5.3) "+round(5.3));
		System.out.println("round(-5.8) "+round(-5.8));
		
		System.out.println("pow(2,5) "+pow(2,5));
		System.out.println("sqrt(pow(2,5)) "+sqrt(pow(2,5)));
	}
	
	
	
}